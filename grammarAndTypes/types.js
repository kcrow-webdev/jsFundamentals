let number = 10;
console.log(number);

//String - strings are datatypes used to represent text
//wrap them in single or double quotes

let stringOne = "double quotes";
let stringTwo = 'single quotes';

console.log(stringOne, stringTwo);

//Booleans - true or false
let on = true;
let off = false;

console.log(on,off);

//Null - an empty value
let empty = null;
console.log(empty);

//Undefined - no value is assigned and does not act as an empty container
let undef = undefined;
console.log(undef);

let correct;
console.log(correct);

//Objects - used to store many values instead of one single value
//denoted by curly brackets {}
let Frodo = {
    race: "hobbit",
    rings: 1,
    cloak: true
}
console.log(Frodo);
console.log(typeof Frodo);

//Arrays - containers that hold lists of items
//denoted by square brackets []

let list = ['item1', 'item2', 'item3'];
console.log(list);

let burritos = ['large', 4, true];
console.log(burritos);

//DataType Literals
//a literal represents a fixed value that we as developers insert into the code
//includes strings, numbers, booleans, objects, and arrays

//string literal
let car = 'Ford';

//numeric literal
let december = 12;

//boolean literal
let tired = true;

//object literal
let soup = {
    a: 'chicken noodle',
    b: 'tomato',
    c: 'beef and barley',
    d: 'chili',
    e: 'cereal'
};

//array literal
let days = ['Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday'];
