//Comparison Operators

//Equal to
'3' == 3;
console.log('3' == 3);

//Strict Equal to
//compares the datatype and value of the two values to check if they are equal

console.log('3' === 3);

//Not equal to
console.log('3' != 3);

//Strict not equal to
console.log('3' !== 3);

//Greater than
console.log(3 > 2);

//Less than
console.log(2 < 3);

//Greater than or equal to
console.log(3 >= 2);

//Less than or equal to
console.log(2 <= 3);

//'And' operator
2 && 3;

//'Or' operator
2 || 3;